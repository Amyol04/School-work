package Demo;

public class Trapezoid{

    public static void main(String [] args){

        int a = 10;
        int b = 10;
        int h = 5;
        int z = a + b;
        double halfZ = z/2;
    
        double area = halfZ *h;
        System.out.println("Area of trapezoid is "+area);

    } // end of main
} // end of class